// Attainuid:#13 Q-1) Fizz Buzz (https://leetcode.com/problems/fizz-buzz/) 
// Given an integer n, return a string array 
// answer (1-indexed) where: ● 
// answer[i] == "FizzBuzz" if i is divisible by 3 and 5.
//  ● answer[i] == "Fizz" if i is divisible by 3. 
//  ● answer[i] == "Buzz" if i is divisible by 5. 
//  ● answer[i] == i (as a string) 


number = Number(require("prompt-sync")()());
var answer =[];
function FizzBuzz(number)
{
    for(i=1;i<=number;i++)
    {
        if(i%3==0 && i%5==0)
        {
           answer.push('FizzBuzz')
           
        }
        else
        {
            if(i%3==0 || i%5==0)
            {
                if(i%3==0)
                 answer.push('Fizz');                   
                else
                 answer.push('Buzz');                   
            }
            else
               answer.push(i.toString());
        }
                
    }  
 console.log((answer));

}
FizzBuzz(number);




