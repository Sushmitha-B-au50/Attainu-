let nums1 = [1,2,3,0,0,0]
let nums2 = [2,5,4]
let m = 3;
let n = 3;



var merge = function(nums1, m, nums2, n) {
    let k = m + n - 1,
    i = m - 1,
    j = n - 1;
   while (j >= 0) {
    if (nums2[j] <= nums1[i]) {
      nums1[k] = nums1[i];
      k--;
      i--;
    } else {
      nums1[k] = nums2[j];
      k--;
      j--;
    }
  }
  return nums1;
};


console.log(merge(nums1,m,nums2,n));


