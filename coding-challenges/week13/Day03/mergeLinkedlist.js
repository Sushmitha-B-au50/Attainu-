var mergeInBetween = function(list1, a, b, list2) {
    let curr = list1;
    let next= curr.next;
    while(next.val != a)
        {
            curr = next;
            next = next.next;
        }
    let farNext = next.next;
    while(farNext.val != b)
        {
            farNext = farNext.next;           
        }
    curr.next = list2;
    while(curr.next != null)
        {
            curr = curr.next;
        }
    curr.next = farNext.next;
    farNext.next = null;
    return list1;
};


