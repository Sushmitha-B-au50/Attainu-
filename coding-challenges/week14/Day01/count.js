class Node {
    constructor(val){
        this.val = val;
        this.left = null;
        this.right = null;
    }
}

let node1 = new Node(2);
let node2 = new Node(1);
let node3 = new Node(3);
let node4 = new Node(4);

//    2
//  1   3
//        4
node1.left = node2;
node3.right = node4;
node1.right = node3;
let root = node1;


var countNodes = function(root) {
    if(root==null) return 0;
    let lsn = countNodes(root.left);
    let rsn = countNodes(root.right);
    return lsn + rsn + 1;
};
console.log(countNodes(root));

