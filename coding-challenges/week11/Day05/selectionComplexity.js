let arr =[19,72,0,8,2,9]


function seletionSort(arr)
{
    for(i=0;i<arr.length;i++)   //o(n)
    {
        for(j=i+1;j<arr.length;j++) //o(n)
      {
        if (arr[j]<arr[i])  
        {
          [arr[j],arr[i]] = [arr[i],arr[j]]
        }
      }
    }
return arr;
}

console.log(seletionSort(arr));  


// TimeComplexity of the selection sort without using any extra array or list is O(n^2)
// because we are using two loops which runs two times size of array n * n is O(n^2)
// spaceComplexity is O(1) in place swapped the array no extra space is used 



