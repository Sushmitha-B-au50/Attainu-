nums = [4,2,4,0,0,3,0,5,1,0];
var moveZeroes = function(nums) {  
    let i = 0;
     let j =i+1;
     while(j<=nums.length-1)
         { 
             if(nums[i]==0)
                {
                    if(nums[j]!==0)
                        {    
                            [nums[i],nums[j]]= [nums[j],nums[i]];
                            i++;
                            j++;
                        }
                    else j++;
                 }
                 else{
                        i++;
                        j++;
                }     
         }
      return nums;  
 };

 console.log(moveZeroes(nums))
