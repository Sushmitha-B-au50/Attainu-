let arr =[99,66,1,8,2,9]


function seletionSort(arr)
{
    let i=0;
    let j;
    while(i<arr.length)
    {
      j = i+1;
      while(j<arr.length)
      {
        if (arr[j]<arr[i])  
        {
          [arr[j],arr[i]] = [arr[i],arr[j]]
        }
        j++;
      }
    i++;
    }
return arr;
}

