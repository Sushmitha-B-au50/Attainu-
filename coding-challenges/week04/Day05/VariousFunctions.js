// / Q-2) Write a function declaration by expression, and convert it into an arrow function? keep
// // the arrow function anonymous.

let difference = function(c,b) // function expression
{
     a = c-b ;
    return a;
}
console.log(difference(3,4));

// arrow function anonymous
let arrowdifference = (c,b) => a = c-b ;
console.log(arrowdifference(3,4));



