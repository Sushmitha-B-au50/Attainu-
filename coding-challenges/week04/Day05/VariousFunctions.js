// function declaration by statement
//defining a function with a name, that is a function declaration.

function multiply(a,b)//function declaration
{
    c= a*b;
    return c;
}
console.log(multiply(3,4));


// function declaration by expression
// if we assign a function without a name to a variable it is called function declaration by expression

let sum = function(c,b) // function expression
{
     a = c+b ;
    return a;
}
console.log(sum(3,4));

