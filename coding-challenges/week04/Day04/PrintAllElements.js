// all elements of an array using any loop
console.log("using while loop");
let arr = [1,2,3,4,5]
let index = 0;
while(index < arr.length)
{
    console.log(arr[index]);
    index++;
}
console.log("-------------------------------------------------------------------")
console.log("using for…of loop");
// using for…of loop.
let SampleArray = [1,9,0,"hello",6]
for(item of SampleArray)
{
    console.log(item);
}



