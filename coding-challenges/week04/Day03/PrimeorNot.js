// Ex: 1 is not a prime number | 17 is a prime number
// Note: Also handle edge cases and bad inputs appropriately


var number = 17;
checkPrime = true;

let Prime = (number)=> {
    if(number == 1)
    {
      checkPrime = false;
    }       
    else if(number>1)
    {
        for(i=2;i<number;i++)
        {
            if(number % i== 0)
             checkPrime = false;       
        }
        
    }
    else
    {
      checkPrime = true;  
    } 
return checkPrime;
}


if(Prime(number))
{
    console.log(number + " is a prime number");
}
else
{
    console.log(number + " is not a prime number");
}


