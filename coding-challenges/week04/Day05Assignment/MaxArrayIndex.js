// Q-1) Write a program to print the index of the maximum value of an array of numbers using
// any loop.
// e.g.:
// Input: arr = [7,5,3,9,1,6,4,8,2]
// output: 3
// Explanation:
// 3 is the index of 9, which is the maximum value of the array


arr = [7,5,3,9,1,6,4,8,2]
let i=0;
max=arr[i] ;
for(i=0;i<arr.length;i++)
{
    if(arr[i]> max)
      max = arr[i];
}
console.log(max);



