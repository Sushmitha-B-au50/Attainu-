Arr = [7,5,3,10,1,6,4,8,2]
EvenNumbers = [];
let EvenNumbersArray = (Arr) =>
{    
    for(item of Arr)
    {
        if(Arr[item] % 2 ==0)
        {
            EvenNumbers.push(Arr[item]);
        } 
    }
    return EvenNumbers;
}

console.log(EvenNumbersArray(Arr));



