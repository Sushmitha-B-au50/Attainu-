Arr = [7,5,3,199,1,6,4,8,2]
max = 0;
let MaxArray = (Arr) =>
{    
    for(item of Arr)
    {
        if(Arr[item]> max)
        {
          max = Arr[item];
        } 
    }
    return max;
}

console.log(MaxArray(Arr))


