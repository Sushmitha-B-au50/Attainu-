// Q-1 ) Program for Sum of the digits of a given number:(5 marks)
// Sample Input:
// 1234567
// Sample output:
// 28

let number = 1234567;
let sum = 0;
function SumofDigits(number){
    if(number==0) return sum;
    else{
        sum += number%10;
        return SumofDigits(Math.floor(number/10));
    } 
}

console.log(SumofDigits(number));

// Q-2 ) Given a number n, find sum of first n natural numbers:(5 marks)
// Examples :
// Input : 5
// Output : 15
// Explanation : 1 + 2 + 3 + 4 + 5 = 15

let N = 5;
let sumOfNatural = 0;
let i =1;
function SumofNatural(N,i){
    if(i>N) return sumOfNatural;
    else{
        sumOfNatural += i;
        return  SumofNatural(N,i+1);
    } 
}
console.log(SumofNatural(N,i));


