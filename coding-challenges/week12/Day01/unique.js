var sumOfUnique = function(nums) {
    NewObj = {};
    let res = 0;
    for(i=0;i<nums.length;i++)
    {  
          let count = 1;
          if(NewObj[nums[i]])
          { 
             NewObj[nums[i]]++;  
          }
          else                               
          {
             NewObj[nums[i]] = count;
          }
    }
    for(a in NewObj)
    {
      if(NewObj[a] ==1)
      {
        res +=Number(a);
      }
    }
   return res ;
};

console.log(sumOfUnique([1,1,1,2])); // output 2;



