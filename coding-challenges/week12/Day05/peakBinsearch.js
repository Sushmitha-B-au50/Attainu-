var findPeakElement = function(nums) {
    let left = 0 ; let right = nums.length-1
    let  mid;
    let peak ;
    if (nums.length==1) return 0;
    else{
    while(left<right)
    {
      mid = Math.floor((left+right)/2);
      peak = mid;
      if(nums[peak]<nums[mid+1])
      {
        peak = nums[mid+1];  
        left=mid+1;
      }
      else{
        right = mid;
      }      
    }
    return left;  
}
 };

 console.log(findPeakElement([1,2]));


