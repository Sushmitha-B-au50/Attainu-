var isValid = function(s) 
{
    let arr = [];   
    for(i=0;i<s.length;i++)
    {
      if((s[i]==")" && arr[arr.length-1] =="(") || (s[i]=="}" && arr[arr.length-1] =="{")
      || (s[i]=="]" && arr[arr.length-1] =="["))
      {
        arr.pop(s[i]);
      }      
      else {
            arr.push(s[i]);
      }
    }
    if(arr.length==0)
    {
        return true;
    }
    else
    {
         return false;
    }
};

console.log(isValid("(]"))

