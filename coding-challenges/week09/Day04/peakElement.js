var findPeakElement = function(nums) {
    let peak = nums[0];
     let index;
     for(i=0;i<nums.length;i++)
         {
             if(nums[i] >= peak)
                 {
                     peak = nums[i];
                     index = i;
                 }
             }
     return index;
 };

