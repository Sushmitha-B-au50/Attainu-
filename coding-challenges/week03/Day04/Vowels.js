// Q-1 )Write a program to print the vowels in a 
// particular str given by the user. (5 marks) 
// Eg 
// Attainu 
// Output: vowels are A, a, i, u

var vowels = "";
str = (require("prompt-sync")()());
for(i=0;i<str.length;i++)
{
    if(str[i] == 'a' || str[i] =='A' || str[i] ==  'e' || str[i] ==  'E' || str[i] == 'i' || str[i] == 'I'|| 
    str[i] == 'o' || str[i] ==  'O' || str[i] ==  'U' || str[i] ==  'u')
    {
        vowels += str[i] + " ";
    }
           
}
console.log('vowels are '+ vowels);




