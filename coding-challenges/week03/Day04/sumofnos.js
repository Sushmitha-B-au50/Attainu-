// sum of n natural numbers .
//  Eg n = 5 (5 marks) 
//  Output 1 + 2 + 3 + 4 + 5 = 15
var result = 0;
number = Number(require("prompt-sync")()());
for(i=1;i<=number;i++)
{
   
     result += i;   
           
}
