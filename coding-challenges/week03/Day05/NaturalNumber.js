// Q-1) write a program to find the sum of the first n natural numbers,
// take the value of n by yourself?

var result = 0;
function NaturalNumber(number)
{
    for(i=1;i<=number;i++)
    {  
        result += i;             
    }
    console.log(result); 
}
NaturalNumber(5);



