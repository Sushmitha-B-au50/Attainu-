// Q-3) FizzBuzz - take n as input from the user, From 1 to n, print Fizz if a number is divisible by 3, 
// print Fuzz 
// if a number is divisible by 5. 
// print FizzFuzz if a number is divisible by 3 and 5 both (5 marks) (Easy) 

number = Number(require("prompt-sync")()());

for(i=1;i<=number;i++)
{
    if(i%3==0 && i%5==0)
    {
        console.log('FizzFuzz');
    }
    else
    {
        if(i%3==0 || i%5==0)
        {
            if(i%3==0)
              console.log('Fizz');
            else
               console.log('Fuzz');
        }
        else
          console.log(i);
    }
         
}


